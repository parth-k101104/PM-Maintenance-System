CREATE TABLE phm_evaluation_audit (
    audit_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    execution_id BIGINT,
    evaluation_date DATE NOT NULL,
    plant_id BIGINT,
    department_id BIGINT,
    line_id BIGINT,
    equipment_id BIGINT,
    part_id BIGINT,
    task_schedule_id BIGINT,
    std_task_id BIGINT,
    evaluation_status VARCHAR(50) NOT NULL,
    reason_code VARCHAR(100),
    data_points_count INTEGER DEFAULT 0,
    current_cycle_points_count INTEGER DEFAULT 0,
    completed_cycles_count INTEGER DEFAULT 0,
    current_value NUMERIC,
    risk_score NUMERIC(5,2),
    metadata JSONB,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT fk_eval_audit_execution
        FOREIGN KEY (execution_id)
        REFERENCES job_execution_history(execution_id)
        ON DELETE SET NULL,

    CONSTRAINT fk_eval_audit_part
        FOREIGN KEY (part_id)
        REFERENCES equipment_parts(part_id)
        ON DELETE CASCADE
);

CREATE INDEX idx_phm_eval_audit_execution ON phm_evaluation_audit(execution_id);
CREATE INDEX idx_phm_eval_audit_lookup ON phm_evaluation_audit(evaluation_date, evaluation_status, part_id);
